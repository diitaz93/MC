<div class="meta">
<div class="list-title">
<span class="descriptor">Title:</span> An integrative statistical model for inferring strain admixture within  clinical Plasmodium falciparum isolates
</div>
<div class="list-authors">
<span class="descriptor">Authors:</span> 
<a href="/find/stat/1/au:+OBrien_J/0/1/0/all/0/1">John D. O&#x27;Brien</a>, 
<a href="/find/stat/1/au:+Iqbal_Z/0/1/0/all/0/1">Zamin Iqbal</a>, 
<a href="/find/stat/1/au:+Amenga_Etego_L/0/1/0/all/0/1">Lucas Amenga-Etego</a>
</div>
<div class="list-subjects">
<span class="descriptor">Subjects:</span> <span class="primary-subject">Applications (stat.AP)</span>; Populations and Evolution (q-bio.PE)

</div>
<p>Since the arrival of genetic typing methods in the late 1960's, researchers
have puzzled at the clinical consequence of observed strain mixtures within
clinical isolates of Plasmodium falciparum. We present a new statistical model
that infers the number of strains present and the amount of admixture with the
local population (panmixia) using whole-genome sequence data. The model
provides a rigorous statistical approach to inferring these quantities as well
as the proportions of the strains within each sample. Applied to 168 samples of
whole-genome sequence data from northern Ghana, the model provides
significantly improvement fit over models implementing simpler approaches to
mixture for a large majority (129/168) of samples. We discuss the possible uses
of this model as a window into within-host selection for clinical and
epidemiological studies and outline possible means for experimental validation.
</p>
</div>
</dd>
