<div class="list-title">
<span class="descriptor">Title:</span> Genetically Improved BarraCUDA
</div>
<div class="list-authors">
<span class="descriptor">Authors:</span> 
<a href="/find/q-bio/1/au:+Langdon_W/0/1/0/all/0/1">W. B. Langdon</a>, 
<a href="/find/q-bio/1/au:+Lam_B/0/1/0/all/0/1">Brian Yee Hong Lam</a>
</div>
<div class="list-comments">
<span class="descriptor">Comments:</span> UCL, Department of Computer Science, technical report RN/15/03
</div>
<div class="list-subjects">
<span class="descriptor">Subjects:</span> <span class="primary-subject">Genomics (q-bio.GN)</span>

</div>
<p>BarraCUDA is a C program which uses the BWA algorithm in parallel with nVidia
CUDA to align short next generation DNA sequences against a reference genome.
The genetically improved (GI) code is up to three times faster on short paired
end reads from The 1000 Genomes Project and 60percent more accurate on a short
BioPlanet.com GCAT alignment benchmark. GPGPU Barracuda running on a single K80
Tesla GPU can align short paired end nextgen sequences up to ten times faster
than bwa on a 12 core CPU.
</p>
</div>
</dd>
<dt><a name="item2">[2]</a>&nbsp;  <span class="list-identifier"><a href="/abs/1505.07866" title="Abstract">arXiv:1505.07866</a> [<a href="/pdf/1505.07866" title="Download PDF">pdf</a>, <a href="/format/1505.07866" title="Other formats">other</a>]</span></dt>
