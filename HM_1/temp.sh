<div class="list-title">
<span class="descriptor">Title:</span> Exploitation of the genomic double-strand breaks to reduce the  reproductive power of microorganisms
</div>
<div class="list-authors">
<span class="descriptor">Authors:</span> 
<a href="/find/q-bio/1/au:+Sassi_G/0/1/0/all/0/1">Giandomenico Sassi</a>, 
<a href="/find/q-bio/1/au:+Sassi_N/0/1/0/all/0/1">Nicola Sassi</a>
</div>
<div class="list-comments">
<span class="descriptor">Comments:</span> 13 pages, 1 figure
</div>
<div class="list-subjects">
<span class="descriptor">Subjects:</span> <span class="primary-subject">Genomics (q-bio.GN)</span>; Biological Physics (physics.bio-ph); Biomolecules (q-bio.BM)

</div>
<p>It is shown how to take advantage of the frequent occurrence of double-strand
breaks in the genome of prokaryotic cells, in order to reduce their high
efficient reproductive capability. The analysis examines the physical status of
the free ends of each break and considers how this status can interfere with an
external physical apparatus, with the aim of undermining the repair processes.
We indicate the biological consequences of this interaction and we give an
approximate evaluation of the topological and dynamical effects that arise on
the genomic material involved. The overall result suggests a significant
reduction of the dynamics of the repair.
</p>
</div>
</dd>
<dt><a name="item5">[5]</a>&nbsp;  <span class="list-identifier"><a href="/abs/1505.08125" title="Abstract">arXiv:1505.08125</a> [<a href="/pdf/1505.08125" title="Download PDF">pdf</a>]</span></dt>
<dd>
<div class="meta">
<div class="list-title">
<span class="descriptor">Title:</span> A possible role for stochastic radiation events in the systematic  disparity between molecular and fossil dates
</div>
<div class="list-authors">
<span class="descriptor">Authors:</span> 
<a href="/find/q-bio/1/au:+Melott_A/0/1/0/all/0/1">Adrian L. Melott</a> (University of Kansas)
</div>
<div class="list-comments">
<span class="descriptor">Comments:</span> Accepted for Earth and Life II
</div>
<div class="list-subjects">
<span class="descriptor">Subjects:</span> <span class="primary-subject">Populations and Evolution (q-bio.PE)</span>; Earth and Planetary Astrophysics (astro-ph.EP); High Energy Astrophysical Phenomena (astro-ph.HE); Medical Physics (physics.med-ph)

</div>
<p>Major discrepancies have been noted for some time between fossil ages and
molecular divergence dates for a variety of taxa. Recently, systematic trends
within avian clades have been uncovered. The trends show that the disparity is
much larger for mitochondrial DNA than for nuclear DNA; also that it is larger
for crown fossil dates than stem fossil dates. It was argued that this pattern
is largely inconsistent with incompleteness of the fossil record as the
principal driver of the disparity. A case is presented that given the expected
mutations from a fluctuating background of astrophysical radiation from such
sources as supernovae, the rate of molecular clocks is variable and should
increase into the past. This is a possible explanation for the disparity. One
test of this hypothesis is to look for an acceleration of molecular clocks 2 to
2.5 Ma due to a probable moderately nearby supernova at that time. Another is
to look for reduced disparity in benthic organisms of the deep ocean.
</p>
</div>
</dd>
